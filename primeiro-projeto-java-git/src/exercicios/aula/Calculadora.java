public class Calculadora {
    /**
     * Este metodo e utilizado para somara dois numeros inteiros
     * @param numeroUm este e o primeiro parametro do metodo
     * @param numeroDois este e o segundo paramentro do metodo
     * @return int o resultado deste metodo e a soma de dois numeros.
     */
    public int somar(int numeroUm, int numeroDois) {
        return numeroUm + numeroDois;
    }
}
